import java.util.Scanner;

// Kelas PenjualanSkincareView untuk menampilkan interaksi dengan pengguna untuk melakukan CRUD pada produk skincare
// Kelas Skincare untuk merepresentasikan produk skincare
public class Skincare {
    private int id;
    private String nama;
    private double harga;
    private int stok;

    public Skincare(int id, String nama, double harga, int stok) {
        this.id = id;
        this.nama = nama;
        this.harga = harga;
        this.stok = stok;
    }

    public int getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public double getHarga() {
        return harga;
    }

    public void setHarga(double harga) {
        this.harga = harga;
    }

    public int getStok() {
        return stok;
    }

    public void setStok(int stok) {
        this.stok = stok;
    }
}
